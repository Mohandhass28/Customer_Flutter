import 'package:customer/domain/product/entities/product_details/product_details_entity.dart';

class ProductDetailsModel extends ProductDetailsEntity {
  const ProductDetailsModel({
    required super.id,
    required super.shopId,
    required super.productCategoryId,
    required super.name,
    required super.description,
    required super.price,
    required super.unit,
    required super.isTaxable,
    required super.cgst,
    required super.sgst,
    required super.cess,
    required super.image,
    required super.status,
    required super.stockStatus,
    required super.isBestSeller,
    required super.isFeatured,
    required super.createdAt,
    super.updatedAt,
    super.deletedAt,
    required super.qty,
    required super.isDiscountedPrd,
    required super.slashPrice,
    required super.isUnpackagePrd,
    required super.unpackagePrdPrice,
    required super.unpackagePrdUnit,
    required super.deliveryMode,
    required super.prdCoverBy,
    required super.prdCoverNum,
    required super.prdCoverDuration,
    super.productFoodType,
  });

  factory ProductDetailsModel.fromJson(Map<String, dynamic> json) {
    return ProductDetailsModel(
      id: json['id'] ?? 0,
      shopId: json['shop_id'] ?? 0,
      productCategoryId: json['product_category_id'] ?? 0,
      name: json['name'] ?? '',
      description: json['description'] ?? '',
      price: json['price'] ?? '0.00',
      unit: json['unit'] ?? '',
      isTaxable: json['is_taxable'] ?? 0,
      cgst: json['cgst'] ?? '0.00',
      sgst: json['sgst'] ?? '0.00',
      cess: json['cess'] ?? '0.00',
      image: json['image'] ?? '',
      status: json['status'] ?? false,
      stockStatus: json['stock_status'] ?? false,
      isBestSeller: json['is_best_seller'] ?? 0,
      isFeatured: json['is_featured'] ?? 0,
      createdAt: json['created_at'] ?? '',
      updatedAt: json['updated_at'],
      deletedAt: json['deleted_at'],
      qty: json['qty'] ?? '0',
      isDiscountedPrd: json['is_discounted_prd'] ?? 0,
      slashPrice: json['slash_price'] ?? '0.00',
      isUnpackagePrd: json['is_unpackage_prd'] ?? 0,
      unpackagePrdPrice: json['unpackage_prd_price'] ?? '0.00',
      unpackagePrdUnit: json['unpackage_prd_unit'] ?? '',
      deliveryMode: json['delivery_mode'] ?? 0,
      prdCoverBy: json['prd_cover_by'] ?? 0,
      prdCoverNum: json['prd_cover_num'] ?? 0,
      prdCoverDuration: json['prd_cover_duration'] ?? 0,
      productFoodType: json['product_food_type'],
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'shop_id': shopId,
      'product_category_id': productCategoryId,
      'name': name,
      'description': description,
      'price': price,
      'unit': unit,
      'is_taxable': isTaxable,
      'cgst': cgst,
      'sgst': sgst,
      'cess': cess,
      'image': image,
      'status': status,
      'stock_status': stockStatus,
      'is_best_seller': isBestSeller,
      'is_featured': isFeatured,
      'created_at': createdAt,
      'updated_at': updatedAt,
      'deleted_at': deletedAt,
      'qty': qty,
      'is_discounted_prd': isDiscountedPrd,
      'slash_price': slashPrice,
      'is_unpackage_prd': isUnpackagePrd,
      'unpackage_prd_price': unpackagePrdPrice,
      'unpackage_prd_unit': unpackagePrdUnit,
      'delivery_mode': deliveryMode,
      'prd_cover_by': prdCoverBy,
      'prd_cover_num': prdCoverNum,
      'prd_cover_duration': prdCoverDuration,
      'product_food_type': productFoodType,
    };
  }
}
